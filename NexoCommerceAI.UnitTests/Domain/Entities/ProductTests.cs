    [Fact]
    public void ChainOperations_ShouldUpdateUpdatedAtEachTime()
    {
        // Arrange
        var product = Product.Create("Laptop", _categoryId);
        var firstUpdate = product.UpdatedAt;
        
        // Act
        System.Threading.Thread.Sleep(10); // Ensure time difference
        product.UpdatePrice(900m);
        var secondUpdate = product.UpdatedAt;
        
        System.Threading.Thread.Sleep(10);
        product.IncreaseStock(5);
        var thirdUpdate = product.UpdatedAt;
        
        // Assert
        Assert.NotEqual(firstUpdate, secondUpdate);
        Assert.NotEqual(secondUpdate, thirdUpdate);
        Assert.True(thirdUpdate > secondUpdate);
        Assert.True(secondUpdate > firstUpdate);
    }
