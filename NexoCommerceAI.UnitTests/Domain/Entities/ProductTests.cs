    [Fact]
    public void Update_WithAllFields_ShouldUpdateProduct()
    {
        // Arrange
        var product = Product.Create("Laptop", _categoryId, price: 999.99m, stock: 5);
        var newCategoryId = Guid.NewGuid();
        var originalUpdatedAt = product.UpdatedAt;
        
        // Act
        product.Update(
            name: "Gaming Laptop",
            slug: "gaming-laptop",
            description: "New description",
            price: 1299.99m,
            compareAtPrice: 1499.99m,
            sku: "NEW-SKU-001",
            stock: 15,
            isFeatured: true,
            categoryId: newCategoryId);
        
        // Assert
        Assert.Equal("Gaming Laptop", product.Name);
        Assert.Equal("gaming-laptop", product.Slug);
        Assert.Equal("New description", product.Description);
        Assert.Equal(1299.99m, product.Price);
        Assert.Equal(1499.99m, product.CompareAtPrice);
        Assert.Equal("NEW-SKU-001", product.Sku);
        Assert.Equal(15, product.Stock);
        Assert.True(product.IsFeatured);
        Assert.Equal(newCategoryId, product.CategoryId);
        Assert.NotNull(product.UpdatedAt);
        Assert.NotEqual(originalUpdatedAt, product.UpdatedAt);
    }
    
